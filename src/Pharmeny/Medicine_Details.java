    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        String keyword = search_out.getText().trim();
        
        try {
            model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
            String query = "SELECT * FROM medicines WHERE name LIKE ? OR category LIKE ? OR supplier LIKE ?";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, "%" + keyword + "%");
            pst.setString(2, "%" + keyword + "%");
            pst.setString(3, "%" + keyword + "%");
            
            ResultSet rs = pst.executeQuery();
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("medicine_id"),
                    rs.getString("name"),
                    rs.getString("category"),
                    rs.getDouble("price"),
                    rs.getInt("quantity"),
                    rs.getDate("expiry_date"),
                    rs.getString("supplier"),
                    rs.getString("status")
                });
            }
            
            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "No medicines found!");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error searching: " + e.getMessage());
        }
    }//GEN-LAST:event_SearchActionPerformed

    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        if (!validateInputs()) return;
        
        try {
            String query = "INSERT INTO medicines (name, category, price, quantity, expiry_date, supplier) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(query);
            
            pst.setString(1, name_out.getText());
            pst.setString(2, cat_out.getText());
            pst.setDouble(3, Double.parseDouble(price_out.getText()));
            pst.setInt(4, Integer.parseInt(qty_out.getText()));
            pst.setDate(5, Date.valueOf(expiry_out.getText()));
            pst.setString(6, supplier_out.getText());
            
            int rows = pst.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Medicine added successfully!");
                clearForm();
                loadTableData();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error adding medicine: " + e.getMessage());
        }
    }//GEN-LAST:event_addActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        if (id_out.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select a medicine to update first!");
            return;
        }
        
        if (!validateInputs()) return;
        
        try {
            String query = "UPDATE medicines SET name=?, category=?, price=?, quantity=?, expiry_date=?, supplier=? WHERE medicine_id=?";
            PreparedStatement pst = conn.prepareStatement(query);
            
            pst.setString(1, name_out.getText());
            pst.setString(2, cat_out.getText());
            pst.setDouble(3, Double.parseDouble(price_out.getText()));
            pst.setInt(4, Integer.parseInt(qty_out.getText()));
            pst.setDate(5, Date.valueOf(expiry_out.getText()));
            pst.setString(6, supplier_out.getText());
            pst.setInt(7, Integer.parseInt(id_out.getText()));
            
            int rows = pst.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Medicine updated successfully!");
                loadTableData();
            }
            } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error updating medicine: " + e.getMessage());
        }
    }//GEN-LAST:event_updateActionPerformed

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        // TODO add your handling code here:
        if (id_out.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select a medicine to delete first!");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete this medicine?", 
            "Confirm Delete", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                String query = "DELETE FROM medicines WHERE medicine_id=?";
                PreparedStatement pst = conn.prepareStatement(query);
                pst.setInt(1, Integer.parseInt(id_out.getText()));
                
                 int rows = pst.executeUpdate();
                if (rows > 0) {
                    JOptionPane.showMessageDialog(this, "Medicine deleted successfully!");
                    clearForm();
                    loadTableData();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error deleting medicine: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_delActionPerformed

