    private boolean validateInputs() {
        if (name_out.getText().trim().isEmpty() ||
            price_out.getText().trim().isEmpty() ||
            qty_out.getText().trim().isEmpty() ||
            expiry_out.getText().trim().isEmpty() ||
            supplier_out.getText().trim().isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "Please fill all required fields!");
            return false;
    }
    //check numeric values
    try {
            double price = Double.parseDouble(price_out.getText());
            if (price <= 0) {
                JOptionPane.showMessageDialog(this, "Price must be greater than 0!");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid price!");
            return false;
        }
        
        try {
            int quantity = Integer.parseInt(qty_out.getText());
            if (quantity < 0) {
                JOptionPane.showMessageDialog(this, "Quantity cannot be negative!");
                return false;
                }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid quantity!");
            return false;
        }
        
    // Check date format
        try {
            Date.valueOf(expiry_out.getText());
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Date format must be YYYY-MM-DD");
            return false;
        }
        
        return true;
    }

