    private void loadTableData(String sortColumn) {

    model.setRowCount(0);

    String sql =
        "SELECT medicine_id, name, category, price, quantity, expiry_date, supplier " +
        "FROM medicines " +
        "WHERE status = 'Active' " +
        "ORDER BY " + sortColumn;

    try (Statement st = conn.createStatement();
         ResultSet rs = st.executeQuery(sql)) {

        while (rs.next()) {
            double price = rs.getDouble("price");
            int qty = rs.getInt("quantity");

            model.addRow(new Object[]{
                rs.getInt("medicine_id"),
                rs.getString("name"),
                rs.getString("category"),
                price,
                qty,
                rs.getDate("expiry_date"),
                rs.getString("supplier"),
                price * qty
            });
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}

    
    private String getSortColumn() {
        return switch (combobox.getSelectedItem().toString()) {
            case "ID" -> "medicine_id";
            case "Name" -> "name";
            case "Category" -> "category";
            case "Price" -> "price";
            case "Quantity" -> "quantity";
            case "Expiry Date" -> "expiry_date";
            case "Supplier" -> "supplier";
            case "Total Value" -> "(price * quantity)";
            default -> "medicine_id";
        };
    }
    
    private void exportToCSV() {
        JFileChooser chooser = new JFileChooser();
        chooser.setSelectedFile(new java.io.File("medicine_stock.csv"));

        if (chooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            try (FileWriter fw = new FileWriter(chooser.getSelectedFile())) {

                for (int i = 0; i < model.getColumnCount(); i++) {
                    fw.write(model.getColumnName(i) + ",");
                }
                fw.write("\n");

                for (int r = 0; r < model.getRowCount(); r++) {
                    for (int c = 0; c < model.getColumnCount(); c++) {
                        fw.write(model.getValueAt(r, c).toString() + ",");
                    }
                    fw.write("\n");
                }

                JOptionPane.showMessageDialog(this, "âœ… Export successful");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Export failed:\n" + e.getMessage());
            }
        }
    }
